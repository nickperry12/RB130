# What are the differences between RSpec and MiniTest?

=begin
Functionally, Minitest can do everything that RSpec can. Where it differs is
Minitest provides a simpler and straightforward syntax, while RSpec allows
developers to write code that reads like natural English, but does so at the
cost of simplicity. RSpec is a Domain Specifc Language for writing tests.

Minitest on the other hand is Ruby's default testing library that is a part of
Ruby's standard library. It's a bundled gem that comes with the default Ruby
installation, and is maintained outside the Ruby core team. It can be
uninstalled if necessary.
=end
