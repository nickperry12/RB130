# What constitutes a Ruby project?

=begin
A project is a collection of one or more files used to develop, test, build and
distribute software. The software could be an executable program, a library
module, or a combination of programs and library files. The project includes the
source code, tests, HTML files, databases configuration files and more.

Ruby based projects are standardized and follow patterns: certain files and
directories must be present, with certain types of data stored in specific
locations. Some data must also be presented in well-defined formats. The most
common standard for Ruby projects is the RubyGems standard.
=end
