# What are Ruby Version Managers and why are they useful?

=begin
Ruby Version Managers are programs that lets us install, manage and use multiple
versions of Ruby. These are useful because at some point we are going to be
writing or using Ruby programs that require a different version of Ruby. Ruby is
an evolving language, and with each update has features that are added and
removed, so some programs may require us to use a version that has features that
may no longer be available in newer releases.

Ruby version managers are also useful for when we're working on multiple
projects/applications. Software applications usually standardize on a specific
version of Ruby in order to make sure developers don't use unsupported language
features. Using Ruby version managers allows us to easily switch between
versions of Ruby when working on different projects that require different
versions.
=end
