# What are Rubygems and why are they useful?

=begin
RubyGems are packages of code that we can download, install and use in our Ruby
programs or from the command line. These gems can provide additional
functionality that isn't originally a part of Ruby. RubyGems also provide
mechanisms to release our own Gems. These Gems can eitherbe packages of code
that we `require` in our Ruby programs, or independent Ruby programs that we can
run (e.g., the `bundle` program from the Bundler gem).
=end
