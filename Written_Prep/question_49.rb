# What is Rake and why is it useful?

=begin
Rake is a RubyGem that allows us to automate many common functions that are
required to build, test, package and install programs. With Rake, we can
automate common tasks such as setting up required environments by creating
directories and files, setting up and initializing databases, running tests, as
well as packaging our applicaton and all of its files for distribution.
Essentially, we can write Rake tasks to automate anything we want to do with our
application during the development, testing and release cycles.
=end
